
# Numeros em Python

# tipo int
# tipo int é um número inteiro
print(10) # inteiro

# tipo float
# tipo float é um número decimal
print(10.5) # float

# tipo complex
# tipo complex é um número complexo
print(10 + 5j) # complex

# tipo bool
# tipo bool é um número booleano
print(True) # booleano
print(False) # booleano

