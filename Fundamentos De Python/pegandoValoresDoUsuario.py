
# Pegando valores do usuário

# input() - Pega o valor do usuário
# int() - Converte o valor para inteiro
# float() - Converte o valor para float
# str() - Converte o valor para string
# bool() - Converte o valor para booleano


# Exemplo de uso do input()
print("Digite seu nome:")
nome = input()
print("Seu nome é:", nome)
print("Digite sua idade:")
idade = input()
print("Sua idade é:", idade)
print("Digite seu peso:")
peso = input()
print("Seu peso é:", peso)

# Exemplo de uso do int()
print("Digite sua idade:")
idade = int(input())
print("Sua idade é:", idade)

# Exemplo de uso do float()
print("Digite seu peso:")
peso = float(input())
print("Seu peso é:", peso)

# Exemplo de uso do str()
print("Digite seu nome:")
nome = str(input())
print("Seu nome é:", nome)

# Exemplo de uso do bool()
print("Digite True ou False:")
bool = bool(input())
print("Seu valor booleano é:", bool)
print("Digite 1 ou 0:")
bool = bool(int(input()))
print("Seu valor booleano é:", bool)
